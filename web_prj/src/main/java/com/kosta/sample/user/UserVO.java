package com.kosta.sample.user;

//엔터티 -> VO는 userDAO인 userInsert로 보낼것이다.
public class UserVO {

	//seq userid uname email, passwd, regdate, grade
	
	private int seq; 
	private String userid;
	private String uname; 
	private String email;
	private String passwd ;
	private String regdate;
	private String grade;
	private boolean loginCheck;
	
	public UserVO() {}
	
	public UserVO(int seq, String userid, String uname, String email, 
			String passwd, String regdate, String grade, boolean loginCheck) {
		super();
		this.seq = seq;
		this.userid = userid;
		this.uname = uname;
		this.email = email;
		this.passwd = passwd;
		this.regdate = regdate;
		this.grade = grade;
		this.loginCheck = loginCheck;
	}

	//public boolean isLoginCheck() {
	public boolean getLoginCheck() {
		return loginCheck;
	}

	public void setLoginCheck(boolean loginCheck) {
		this.loginCheck = loginCheck;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

	public int getSeq() {
		return seq;
	}

	public String getUserid() {
		return userid;
	}

	public String getUname() {
		return uname;
	}

	public String getEmail() {
		return email;
	}

	public String getPasswd() {
		return passwd;
	}

	public String getRegdate() {
		return regdate;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}

	@Override
	public String toString() {
		return "UserVO [seq=" + seq + ", userid=" + userid + ", uname=" + uname + ", email=" + email + ", passwd="
				+ passwd + ", regdate=" + regdate + ", grade=" + grade + ", loginCheck=" + loginCheck + "]";
	}

	
	
}
